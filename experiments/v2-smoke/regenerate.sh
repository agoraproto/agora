#!/usr/bin/env bash
# Sprint 38b -- V2 Live Smoke-Test
# Reads every Pre-Mainnet-relevant V2 state point and produces V2_LIVE_STATE.md
# in markdown format on stdout. Capture with:
#
#   Get-Content sprint38b-v2-smoke.sh -Raw | ssh root@188.245.39.250 bash > apps\backend\docs\V2_LIVE_STATE.md

V2=0x0e8E6A760c76cA92c5C5dA06d293E33f1B5fbAEc
SAFE=0x8ec63fe30dab84308b5009b8d91d9e4deb5a61fc
USDC=0x036CbD53842c5426634e7929541eC2318f3dCF7e
DEPLOYER=0xe0f9615B8C63574eB9c0CAf22438Daa4Ac911A03
COSIGNER1=0x3D1A23CBfC53D1d99C91189df7B478d6e97Ea8c0
COSIGNER2=0x2eb03ba6b0D0B72Eb359a53C31Ba46956BAEf14c
RPC=https://sepolia.base.org
CAST=/root/.foundry/bin/cast

# Helper: read a contract value or return "<n/a -- revert>"
read_or_err() {
    local out
    out=$("$@" 2>&1)
    if echo "$out" | grep -qi "error\|revert\|failed"; then
        echo "ERROR: $(echo $out | head -c 100)"
    else
        echo "$out"
    fi
}

NOW=$(date -u +'%Y-%m-%dT%H:%M:%SZ')

# -- Read everything we need ---------------------------------------
V2_OWNER=$($CAST call $V2 "owner()(address)" --rpc-url $RPC)
V2_PENDING=$($CAST call $V2 "pendingOwner()(address)" --rpc-url $RPC)
V2_PAUSED=$($CAST call $V2 "paused()(bool)" --rpc-url $RPC)
V2_TOTAL=$($CAST call $V2 "totalEscrowed()(uint256)" --rpc-url $RPC)
V2_FEE_BPS=$($CAST call $V2 "feeBps()(uint16)" --rpc-url $RPC)
V2_MIN_FEE=$($CAST call $V2 "minFee()(uint256)" --rpc-url $RPC)
V2_MAX_FEE=$($CAST call $V2 "maxFee()(uint256)" --rpc-url $RPC)
V2_INS_BPS=$($CAST call $V2 "insuranceShareBps()(uint16)" --rpc-url $RPC)
V2_FEE_RECIPIENT=$($CAST call $V2 "feeRecipient()(address)" --rpc-url $RPC)
V2_INS_POOL=$($CAST call $V2 "insurancePool()(address)" --rpc-url $RPC)
V2_NEXT_JOB=$($CAST call $V2 "nextJobId()(uint256)" --rpc-url $RPC)

SAFE_THRESHOLD=$($CAST call $SAFE "getThreshold()(uint256)" --rpc-url $RPC)
SAFE_OWNERS=$($CAST call $SAFE "getOwners()(address[])" --rpc-url $RPC)
SAFE_NONCE=$($CAST call $SAFE "nonce()(uint256)" --rpc-url $RPC)

# previewFee probe -- should return a tiny non-zero value
PREVIEW_0_1=$($CAST call $V2 "previewFee(uint256)(uint256)" 100000 --rpc-url $RPC 2>&1)
PREVIEW_1_0=$($CAST call $V2 "previewFee(uint256)(uint256)" 1000000 --rpc-url $RPC 2>&1)

# computeFee probe -- must NOT exist on V2 (V1 legacy)
COMPUTE_FEE=$($CAST call $V2 "computeFee(uint256)(uint256)" 100000 --rpc-url $RPC 2>&1)
COMPUTE_FEE_RESULT="UNKNOWN"
if echo "$COMPUTE_FEE" | grep -qi "execution reverted\|error"; then
    COMPUTE_FEE_RESULT="reverts (correct -- V2 has no computeFee)"
else
    COMPUTE_FEE_RESULT="UNEXPECTEDLY RETURNED: $COMPUTE_FEE"
fi

# refundExpired probe -- call against a non-existent jobId; should revert with a known reason
REFUND_PROBE=$($CAST call $V2 "refundExpired(uint256)" 999999999 --rpc-url $RPC 2>&1)
REFUND_RESULT="UNKNOWN"
if echo "$REFUND_PROBE" | grep -qi "execution reverted"; then
    REFUND_RESULT="reverts cleanly for non-existent job (function exists)"
else
    REFUND_RESULT="unexpected: $REFUND_PROBE"
fi

# Backend config (read from .env)
BACKEND_ESCROW=$(grep ^ESCROW_CONTRACT_ADDRESS /opt/agora/apps/backend/.env | cut -d= -f2)
BACKEND_ABI_VERSION=$(grep ^ESCROW_ABI_VERSION /opt/agora/apps/backend/.env | cut -d= -f2)

# Chain watcher health (from DB + logs)
JOBS_V2=$(sudo -u postgres psql agora -tAc "SELECT COUNT(*) FROM jobs WHERE escrow_contract_address='0x0e8E6A760c76cA92c5C5dA06d293E33f1B5fbAEc'")
JOBS_V1=$(sudo -u postgres psql agora -tAc "SELECT COUNT(*) FROM jobs WHERE escrow_contract_address='0xCE783B527C83c4fFFF3D3565c0F3C3204be02B76'")
JOBS_NULL=$(sudo -u postgres psql agora -tAc "SELECT COUNT(*) FROM jobs WHERE settlement_mode='onchain' AND escrow_contract_address IS NULL")
WATCHER_ERRORS_5MIN=$(journalctl -u agora-api --since '5 minutes ago' --no-pager 2>/dev/null | grep -c 'chain_watcher.unknown_status' || true)

API_STATE_CODE=$(curl -s -o /dev/null -w "%{http_code}" https://api.agoraproto.org/v1/state)

# USDC balances
V1_USDC_HELD=$($CAST call $USDC "balanceOf(address)(uint256)" 0xCE783B527C83c4fFFF3D3565c0F3C3204be02B76 --rpc-url $RPC)
V2_USDC_HELD=$($CAST call $USDC "balanceOf(address)(uint256)" $V2 --rpc-url $RPC)

# Alembic / DB head
ALEMBIC_VERSION=$(cd /opt/agora/apps/backend && /opt/agora/apps/backend/.venv/bin/alembic current 2>&1 | grep -oE '00[0-9]+' | head -1)

# -- Generate the markdown doc on stdout --------------------------
